import argparse
import math
import os
import random
from typing import List, Set, Tuple


# ---------------------------------------------------------------------------
# Distance helpers
# ---------------------------------------------------------------------------


def euclidean(p1: Tuple[float, float], p2: Tuple[float, float]) -> float:
    return math.sqrt((p1[0] - p2[0]) ** 2 + (p1[1] - p2[1]) ** 2)


def build_distance_matrix(coords: List[Tuple[float, float]], scale: int) -> List[List[int]]:
    """Return a symmetric integer distance matrix (scaled Euclidean)."""
    n = len(coords)
    dist = [[0] * n for _ in range(n)]
    for i in range(n):
        for j in range(i + 1, n):
            d = round(euclidean(coords[i], coords[j]) * scale)
            dist[i][j] = d
            dist[j][i] = d
    return dist


# ---------------------------------------------------------------------------
# Random graph construction
# ---------------------------------------------------------------------------


def random_graph_edges(n: int, p: float, rng: random.Random) -> Set[Tuple[int, int]]:
    """
    Return directed edges (i, j) generated independently with probability p
    for each ordered pair i != j.
    """
    edges: Set[Tuple[int, int]] = set()
    for i in range(n):
        for j in range(n):
            if i != j and rng.random() < p:
                edges.add((i, j))
    return edges


# ---------------------------------------------------------------------------
# MiniZinc writer – minimisation model (paper Fig. 1 style)
# ---------------------------------------------------------------------------


MZN_TEMPLATE = """\
%% Circuit-constraint benchmark instance
%% Generated by generate_random_instances.py
%% Method: directed Erdős–Rényi random graph benchmark
%%
%% Model: minimise the length of the longest leg (maxleg).
%%        Successor variables must form a Hamiltonian circuit using only
%%        edges present in the transport network (travelTime >= 0).
%%
%% Instance parameters
%%   n              = {n}
%%   p              = {p:.4f}
%%   seed           = {seed}
%%   edge_count     = {edge_count}   (directed edges)
%%   density        = {density:.4f}  (edge_count / (n * (n - 1)))
%%
%% NOTE: this generator does NOT guarantee feasibility.
%% It is intended as a more completely random benchmark family
%% for testing generalisability beyond geometric k-nearest graphs.


include "globals.mzn";


int: n = {n};
set of int: Locations = 1..n;


%% Maximum possible travel time; used as the upper bound for maxleg.
int: maxLegLen = {max_leg_len};


%% Travel times between locations (–1 means no direct connection exists).
array[Locations, Locations] of int: travelTime =
  array2d(Locations, Locations, [ {travel_time_rows} ]);


%% Successor variables: succ[i] = next location after i in the tour.
array[Locations] of var Locations: succ;


%% Only use allowed legs (edges present in the transport network).
constraint forall(loc1, loc2 in Locations)(
  travelTime[loc1, loc2] < 0 -> succ[loc1] != loc2
);


%% Successors must form a Hamiltonian circuit.
constraint circuit(succ);


%% Variable for the length of the longest leg.
var 1..maxLegLen: maxleg;


%% Link maxleg to actual travel times used in the tour.
constraint forall(loc1, loc2 in Locations)(
  succ[loc1] = loc2 -> maxleg >= travelTime[loc1, loc2]
);


solve :: int_search(succ, input_order, indomain_min)
      minimize maxleg;


output [
  "succ = ", show(succ), "\\n",
  "maxleg = ", show(maxleg), "\\n"
];
"""


def format_travel_time(dist: List[List[int]], edges: Set[Tuple[int, int]], n: int):
    flat = []
    max_tt = 0
    for i in range(n):
        for j in range(n):
            if i == j:
                flat.append(-1)
            elif (i, j) in edges:
                tt = dist[i][j]
                flat.append(tt)
                if tt > max_tt:
                    max_tt = tt
            else:
                flat.append(-1)
    return ", ".join(str(x) for x in flat), max_tt


def write_mzn(
    filepath: str,
    n: int,
    p: float,
    seed: int,
    dist: List[List[int]],
    edges: Set[Tuple[int, int]],
) -> None:
    edge_count = len(edges)
    density = edge_count / (n * (n - 1)) if n > 1 else 0.0
    travel_time_rows, max_leg_len = format_travel_time(dist, edges, n)
    content = MZN_TEMPLATE.format(
        n=n,
        p=p,
        seed=seed,
        edge_count=edge_count,
        density=density,
        max_leg_len=max_leg_len,
        travel_time_rows=travel_time_rows,
    )
    with open(filepath, "w", encoding="utf-8") as fh:
        fh.write(content)


# ---------------------------------------------------------------------------
# Core generation function
# ---------------------------------------------------------------------------


def generate_instance(n: int, p: float, seed: int, scale: int):
    rng = random.Random(seed)
    coords = [(rng.random(), rng.random()) for _ in range(n)]
    dist = build_distance_matrix(coords, scale)
    edges = random_graph_edges(n, p, rng)
    return dist, edges


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Generate random Circuit-constraint benchmark instances "
            "using a directed Erdős–Rényi G(n,p) model and write MiniZinc files. "
            "Model: minimise the length of the longest leg."
        ),
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("-n", "--nodes", type=int, default=50,
                        help="Number of locations")
    parser.add_argument("-p", type=float, default=0.10,
                        help="Arc probability in G(n,p)")
    parser.add_argument("-c", "--count", type=int, default=1,
                        help="Number of instances to generate")
    parser.add_argument("-s", "--seed", type=int, default=None,
                        help="Base random seed (random if omitted)")
    parser.add_argument("-o", "--outdir", type=str, default=".",
                        help="Output directory")
    parser.add_argument("--scale", type=int, default=1000,
                        help="Distance scale factor applied to Euclidean distances")
    parser.add_argument("--prefix", type=str, default="random_instance",
                        help="Filename prefix")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    os.makedirs(args.outdir, exist_ok=True)

    base_seed = args.seed if args.seed is not None else random.randrange(2 ** 32)

    print(f"Generating {args.count} instance(s) (directed G(n,p), no feasibility guarantee):")
    print(f"  n={args.nodes}, p={args.p}, scale={args.scale}")
    print(f"  base seed={base_seed}, output dir='{args.outdir}'")
    print()

    for idx in range(args.count):
        seed = base_seed + idx
        dist, edges = generate_instance(args.nodes, args.p, seed, args.scale)
        edge_count = len(edges)
        density = edge_count / (args.nodes * (args.nodes - 1)) if args.nodes > 1 else 0.0

        filename = f"{args.prefix}_n{args.nodes}_p{args.p:.3f}_{idx:04d}.mzn"
        filepath = os.path.join(args.outdir, filename)

        write_mzn(
            filepath=filepath,
            n=args.nodes,
            p=args.p,
            seed=seed,
            dist=dist,
            edges=edges,
        )

        print(
            f"  [{idx:4d}] seed={seed:10d}  edge_count={edge_count:5d}  "
            f"density={density:.3f}  -> {filepath}"
        )

    print("\nDone.")


if __name__ == "__main__":
    main()